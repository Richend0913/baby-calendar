def json_ld_article(title, description, url, date_pub="2026-03-24", date_mod="2026-03-24"):
    return json.dumps({
        "@context": "https://schema.org",
        "@type": "Article",
        "headline": title,
        "description": description,
        "url": url,
        "datePublished": date_pub,
        "dateModified": date_mod,
        "author": {"@type": "Organization", "name": SITE_NAME},
        "publisher": {
            "@type": "Organization",
            "name": SITE_NAME,
            "logo": {"@type": "ImageObject", "url": f"{SITE_URL}/css/logo.png"}
        },
        "mainEntityOfPage": {"@type": "WebPage", "@id": url},
    }, ensure_ascii=False)


def json_ld_breadcrumb(items):
    """items: list of (name, url) tuples"""
    item_list = []
    for i, (name, url) in enumerate(items, 1):
        item_list.append({
            "@type": "ListItem",
            "position": i,
            "name": name,
            "item": url
        })
    return json.dumps({
        "@context": "https://schema.org",
        "@type": "BreadcrumbList",
        "itemListElement": item_list
    }, ensure_ascii=False)


